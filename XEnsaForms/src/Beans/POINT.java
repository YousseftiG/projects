package Beans;

/**
 *
 * @author Nora
 */
public class POINT {
    private double x,y;

    public POINT(double x, double y) {
        this.x = x;
        this.y = y;
    }

    public POINT() {
        x=y=0;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }

    @Override
    public String toString() {
        return "Point{" + "x=" + x + ", y=" + y + '}';
    }
    
}
